// ---------------------------------------------------------------------------
// Job model (stable fields)
// ---------------------------------------------------------------------------

export interface Job {
  /** Unique local ID (generated by storage helpers) */
  id: string;
  /** Job title */
  title: string;
  /** Federal agency name */
  agency: string;
  /** Duty location(s) */
  location: string;
  /** GS grade or pay band (optional) */
  grade?: string;
  /** Salary range string (optional) */
  salaryRange?: string;
  /** USAJOBS URL or announcement link (optional) */
  url?: string;
  /** Short summary or description (optional) */
  summary?: string;
  /** ISO 8601 timestamp when saved locally */
  savedAt: string;
}

// ---------------------------------------------------------------------------
// Store shape
// ---------------------------------------------------------------------------

export const JOB_SEARCH_SCHEMA_VERSION = 1;

export interface JobSearchStore {
  schemaVersion: number;
  savedJobs: Job[];
  /** ID of the currently selected/viewed job */
  selectedJobId: string | null;
  /** Last search query (for state restoration) */
  lastSearchQuery: string;
}

